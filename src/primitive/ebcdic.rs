/// Translates bytes through a 256-entry lookup table, writing to `output`.
/// Translates `min(output.len(), input.len())` bytes.
#[inline(always)]
#[cfg_attr(all(not(debug_assertions), feature = "no-panic"), no_panic)]
pub fn translate_bytes(output: &mut [u8], input: impl AsRef<[u8]>, table: &[u8; 256]) {
    for (o, i) in output.iter_mut().zip(input.as_ref()) {
        *o = table[*i as usize];
    }
}

/// Translates bytes through a 256-entry lookup table in place.
#[inline(always)]
#[cfg_attr(all(not(debug_assertions), feature = "no-panic"), no_panic)]
pub fn translate_bytes_inplace(buf: &mut [u8], table: &[u8; 256]) {
    for b in buf {
        *b = table[*b as usize];
    }
}

#[inline(always)]
#[cfg_attr(all(not(debug_assertions), feature = "no-panic"), no_panic)]
pub fn encode_ebcdic_1142_char(ch: char) -> Option<u8> {
    if ch.is_ascii() {
        return Some(ASCII_TO_EBCDIC_1142[ch as usize]);
    }
    let code = u32::from(ch);
    if code > u16::MAX as u32 {
        return None;
    }
    let code = code as u16;
    let mut i = 0usize;
    while i < EBCDIC_1142_TO_UNICODE.len() {
        if EBCDIC_1142_TO_UNICODE[i] == code {
            return Some(i as u8);
        }
        i += 1;
    }
    None
}

#[inline(always)]
#[cfg_attr(all(not(debug_assertions), feature = "no-panic"), no_panic)]
pub fn utf8_to_ebcdic_1142<'a>(output: &mut &'a mut [u8], input: &[u8]) -> Result<&'a mut [u8], Error> {
    if input.is_ascii() {
        let buf = output.split_off_mut(..input.len()).ok_or_else(|| {
            cold_path();
            Error::BufferOverflow
        })?;
        translate_bytes(buf, input, &ASCII_TO_EBCDIC_1142);
        return Ok(buf);
    }

    utf8_to_ebcdic_1142_slow(output, input)
}

#[inline(always)]
#[cfg_attr(all(not(debug_assertions), feature = "no-panic"), no_panic)]
pub fn ebcdic_1142_to_utf8<'a>(output: &mut &'a mut [u8], input: &[u8]) -> Result<&'a mut [u8], Error> {
    let mut all_ascii = true;
    for &byte in input {
        if EBCDIC_1142_TO_ASCII_SUBSET[byte as usize] == 0x80 {
            all_ascii = false;
            break;
        }
    }
    if all_ascii {
        let buf = output.split_off_mut(..input.len()).ok_or_else(|| {
            cold_path();
            Error::BufferOverflow
        })?;
        for (dst, &src) in buf.iter_mut().zip(input) {
            *dst = EBCDIC_1142_TO_ASCII_SUBSET[src as usize];
        }
        return Ok(buf);
    }

    ebcdic_1142_to_utf8_slow(output, input)
}

#[cold]
#[inline(never)]
fn utf8_to_ebcdic_1142_slow<'a>(output: &mut &'a mut [u8], input: &[u8]) -> Result<&'a mut [u8], Error> {
    let text = core::str::from_utf8(input).map_err(|_| {
        cold_path();
        Error::Invalid
    })?;
    let mut char_len = 0usize;
    for ch in text.chars() {
        if encode_ebcdic_1142_char(ch).is_none() {
            cold_path();
            return Err(Error::Invalid);
        }
        char_len += 1;
    }
    let buf = output.split_off_mut(..char_len).ok_or_else(|| {
        cold_path();
        Error::BufferOverflow
    })?;
    for (dst, ch) in buf.iter_mut().zip(text.chars()) {
        *dst = encode_ebcdic_1142_char(ch).ok_or_else(|| {
            cold_path();
            Error::Invalid
        })?;
    }
    Ok(buf)
}

#[cold]
#[inline(never)]
fn ebcdic_1142_to_utf8_slow<'a>(output: &mut &'a mut [u8], input: &[u8]) -> Result<&'a mut [u8], Error> {
    let mut used = 0usize;
    let mut i = 0usize;
    while i < input.len() {
        let ch = char::from_u32(EBCDIC_1142_TO_UNICODE[input[i] as usize] as u32).ok_or_else(|| {
            cold_path();
            Error::Internal
        })?;
        let mut utf8 = [0u8; 4];
        let encoded = ch.encode_utf8(&mut utf8).as_bytes();
        let end = used.checked_add(encoded.len()).ok_or_else(|| {
            cold_path();
            Error::BufferOverflow
        })?;
        let buf = output.get_mut(used..end).ok_or_else(|| {
            cold_path();
            Error::BufferOverflow
        })?;
        buf.copy_from_slice(encoded);
        used = end;
        i += 1;
    }
    output.split_off_mut(..used).ok_or_else(|| {
        cold_path();
        Error::BufferOverflow
    })
}

